import matplotlib.pyplot as plt 

# x-axis values - spatial coordinates within the cluster 
x = [i for i in range(0, 10)]

# y-axis values - number of Type A, B and C galaxies 
y1 = [3, 5, 8, 7, 6, 4, 3, 2, 1, 1] # Type A
y2 = [1, 2, 4, 6, 7, 6, 4, 3, 2, 1] # Type B
y3 = [0, 1, 3, 5, 5, 4, 3, 2, 1, 0] # Type C

# Create stack plot
plt.stackplot(x, y1, y2, y3, colors=['blue', 'green', 'red'])

# Adding legend
plt.legend(['Type A galaxies', 'Type B galaxies', 'Type C galaxies'], loc='upper right')

# Adding title and labels 
plt.title("Spatial Distribution of Galaxies within a Cluster")
plt.xlabel("Spatial coordinates within the cluster")
plt.ylabel("Number of galaxies")


