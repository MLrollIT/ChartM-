import matplotlib.pyplot as plt

# Age Groups
age_groups = ['18-25', '26-35', '36-45', '46-55', '56-65']

# Social Media Usage (hours per day)
social_media_usage = [2, 3, 4, 6, 5]

# Happiness Level (on a scale of 1-10)
happiness_level = [7, 6, 5, 4, 3]

# Create a scatter plot
plt.scatter(social_media_usage, happiness_level, color='b')

# Label each data point with its age group
for i in range(len(age_groups)):
    plt.text(social_media_usage[i], happiness_level[i], age_groups[i])

# Add labels to the axes and a title
plt.xlabel('Social Media Usage (hours per day)')
plt.ylabel('Happiness Level (on a scale of 1-10)')
plt.title('Impact of Social Media Usage on Happiness Levels Among Different Age Groups')
plt.tight_layout()
plt.savefig("figure.png")

