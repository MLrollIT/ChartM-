import matplotlib.pyplot as plt

years = [2015, 2016, 2017, 2018, 2019, 2020]

NAmerica = [2000, 2100, 2300, 2400, 2500, 2600]
Europe = [2500, 2600, 2700, 2800, 2900, 3000]
Asia = [1500, 1600, 1800, 1900, 2100, 2300]
SAmerica = [3000, 3100, 3300, 3500, 3700, 3900]

plt.figure(figsize=[10, 6])

plt.stackplot(years, NAmerica, Europe, Asia, SAmerica, colors=['blue', 'green', 'yellow', 'red'], labels=['North America','Europe','Asia','South America'])

plt.title('Coffee consumption patterns worldwide')
plt.xlabel('Year')
plt.ylabel('Coffee consumption (in million kg)')
plt.legend(loc='upper left')

plt.tight_layout()
plt.savefig("figure.png")

